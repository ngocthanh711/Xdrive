class Trip {
  final String time;
  final String date;
  final String from;
  final String to;
  final int price;
  final int oldPrice;
  final String status;

  Trip({
    required this.time,
    required this.date,
    required this.from,
    required this.to,
    required this.price,
    required this.oldPrice,
    required this.status,
  });
}
